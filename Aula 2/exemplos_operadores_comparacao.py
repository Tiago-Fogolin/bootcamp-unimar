# uma comparação no python, sempre retornará True ou False
a = 10
b = 5
c = 10

# Igualdade
print("a == b:", a == b)   # False
print("a == c:", a == c)   # True

# Diferença
print("a != b:", a != b)   # True
print("a != c:", a != c)   # False

# Maior / Menor
print("a > b:", a > b)     # True
print("a < b:", a < b)     # False

# Maior ou igual / Menor ou igual
print("a >= c:", a >= c)   # True
print("b <= a:", b <= a)   # True